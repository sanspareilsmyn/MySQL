/*
AB->C, C->A

1) 릴레이션 R의 후보키를 찾아보시오.
AB->C이므로 AB -> ABC도 만족한다. 따라서 후보키는 AB이다.
마찬가지로 BC -> ABC이므로 BC도 후보키이다.

2) 릴레이션 R은 몇 정규형인가>
제 2정규형 만족. (AB->C라고 해서 A->C거나 B->C가 아니잖아.)
제 3정규형 만족. (AB -> C -> A는 비이행적.)
그런데 C->A 에서 C는 후보키가 아니므로 BCNF가 아니다. 따라서 제3정규형이다.

3) 릴레이션을 다음과 같이 분해했을 때 무손실 분해인가?
R1(A, C), R2(B, C)
C가 공통속성인데, C가 R1의 후보키이므로 무손실 분해이다. (C->A)
